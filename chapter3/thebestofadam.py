import turtle
turtle.Screen().bgcolor("blue")
turtle.Screen().setup(600,700)
t=turtle.Turtle()
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.forward(100)
t.right(120)
t.penup()
t.right(90)
t.forward(50)
t.pendown()
t.left(90)
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
t.left(120)
t.forward(100)
turtle.done()